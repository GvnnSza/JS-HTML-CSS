let num = [5 , 8 , 9 , 2]

num.sort() // ! ORDENA OS ELEMENTOS DO ARRAY EM ORDEM CRESCENTE [2, 5, 8, 9]

num.push(1) // ! ADICIONA O ELEMENTO 1 NO FINAL DO ARRAY [2, 5, 8, 9, 1]

console.log(num) // ! MOSTRA O ARRAY [2, 5, 8, 9, 1]

console.log(`O vetor tem ${num.length} posições`) // ! MOSTRA A QUANTIDADE DE ELEMENTOS DO ARRAY (5)

console.log(`O primeiro valor do vetor é ${num[0]}`) // ! MOSTRA O PRIMEIRO ELEMENTO DO ARRAY (2)